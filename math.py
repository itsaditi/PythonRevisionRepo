"""
Basic Arithmetic Operations
"""
# Division returns a float
div = 2 / 3 
print(round(div, 2))

# Floor division returns int, 
# discarding fractional part
floor_division = 5 // 3

# Modulo returns remainder of division
modulo = 2 % 3

# Exponentiation
square = 3 ** 2
cube = 2 ** 3

print(floor_division)
print(modulo)
print(square)
print(cube)

"""
Built-in math operations
"""
nums = [5, 10, 2, 25, 8, 12]
print(min(nums))
print(max(nums))

print(abs(-3.14))
print(round(div, 2))
print(pow(10, 2))



